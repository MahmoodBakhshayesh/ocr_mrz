import 'package:ocr_mrz/mrz_result_class_fix.dart';

enum DocumentStandardType {
  td1,
  td2,
  td3;

  @override
  toString() {
    return name.toUpperCase();
  }
}

extension DocumentStandardTypeDetails on DocumentStandardType {
  MrzFormat get getFormat {
    switch (this) {
      case DocumentStandardType.td1:
        return MrzFormat.TD1;
      case DocumentStandardType.td2:
        return MrzFormat.TD2;
      case DocumentStandardType.td3:
        return  MrzFormat.TD3;
    }
  }
}
